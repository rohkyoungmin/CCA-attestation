/*
 * vecu_comm.c — V-ECU Inter-VM Communication Benchmark
 *
 * Measures communication overhead between simulated V-ECUs:
 *   Phase 0: Baseline plain TCP echo (no TLS)
 *   Phase 1: TLS-only handshake + echo
 *   Phase 2: TLS + per-message attestation token check
 *
 * Architecture simulation (on localhost):
 *   V-ECU1 (Zephyr/Realm)  ←→ PORT 9001 (baseline TCP)
 *   V-ECU2 (AGL/Normal)    ←→ PORT 9002 (TLS)
 *   V-ECU3 (Linux/Normal)  ←→ PORT 9003 (TLS + attest)
 *
 * Metrics:
 *   - TLS handshake time (us)
 *   - Per-message RTT (us)  [RTT/2 = one-way latency]
 *   - CPU overhead via /proc/self/stat (jiffies)
 *   - Throughput (msgs/sec)
 *
 * Build: gcc -O2 -o vecu_comm vecu_comm.c -lssl -lcrypto -lpthread
 */
