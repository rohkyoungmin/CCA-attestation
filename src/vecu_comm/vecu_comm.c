        connect(fd, (struct sockaddr*)&addr, sizeof(addr));

        char msg[MSG_SIZE], reply[MSG_SIZE];
        RAND_bytes((unsigned char*)msg, MSG_SIZE);
        long cpu_before = read_cpu_jiffies();
